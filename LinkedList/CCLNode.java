package LinkedList;

public class CCLNode {

	int data;
	CCLNode next;
	
	public CCLNode(int data){
		this.data = data;
		this.next = next;
	}
	
	public CCLNode(){
		
	}
	
	public void setData(int data){
		this.data = data;
	}
	
	public int getData(){
		return this.data;
	}
	
	public void setNext(CCLNode next){
		this.next = next;
	}
	
	public CCLNode getNext(){
		return this.next;
	}
}
